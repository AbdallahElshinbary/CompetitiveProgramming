double SBC(string s) {
	double x = 0;
	for(int i=1; i<=(int)s.size(); i++) x += i*mp[s[i-1]];
	return x;
}

void rec(int n, int i, string temp) {
	if(i-1 == n) {
		cnt[n-1][temp[0]-'a']++;
		avg[n-1][temp[0]-'a'] += SBC(temp);
		return;
	}

	if(i%2 != 0) {
		for(char c : odd) {
			if(freq[c-'a'] < 2) {
				freq[c-'a']++;
				rec(n, i+1, temp+c);
				freq[c-'a']--;
			}

		}
	} else {
		for(char c : even) {
			if(freq[c-'a'] < 2) {
				freq[c-'a']++;
				rec(n, i+1, temp+c);
				freq[c-'a']--;
			}
		}
	}
}



